def show_directions(maze_stack :stack, index_i :int, indx_j :int, pre_i :int, pre_j :int):
    if maze_stack[index_i-1][indx_j] != maze_stack[pre_i][pre_j] and maze_stack[index_i-1][indx_j] == 0 :
        return 'North'
    elif maze_stack[index_i-1][indx_j] != maze_stack[pre_i][pre_j] and maze_stack[index_i][indx_j+1] == 0 :
        return 'East'
    elif maze_stack[index_i-1][indx_j] != maze_stack[pre_i][pre_j] and maze_stack[index_i+1][indx_j] == 0 :
        return 'South'
    elif maze_stack[index_i-1][indx_j] != maze_stack[pre_i][pre_j] and maze_stack[index_i-1][indx_j-1] == 0 :
        return 'West'
    else :
        return 'stuck'






