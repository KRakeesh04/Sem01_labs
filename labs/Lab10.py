class stack :
    def main(self):
        self.items = []

    def pop(self):
        self.items.pop()

    def push(self):
        self.items.append()

    def is_empty(self):
        return len(self.items) == 0
    
    def peek(self):
        if not self.is_empty(self):
            return self.items[-1]
        else :
            print("stack is empty")


